    public void load_pt(){
        try {
            String sql = "SELECT * FROM `pt`";
            Connection conn = (Connection)Config.configDB();
            Statement stm = conn.createStatement();
            ResultSet res = stm.executeQuery(sql);
            while (res.next()) {                
                jComboBox1.addItem(res.getString("nama_PT"));
            }
            res.close();
            stm.close();
        } catch (Exception e) {
        }
    }

